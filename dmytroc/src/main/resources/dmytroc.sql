-- SQL_JOIN_tasks -task 9
SELECT e.ename, e.job, d.dname, e.sal, s.grade
FROM emp AS e
JOIN dept AS d ON d.deptno = e.deptno
JOIN salgrade AS s ON e.sal BETWEEN s.losal AND s.hisal
ORDER BY d.dname;

